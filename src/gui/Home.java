    private void countAccounts() {

        try {
            ResultSet resultSet = MySQL.execute("SELECT COUNT(id) FROM `account`WHERE `status_id`='1'");

            if (resultSet.next()) {

                jLabel6.setText(resultSet.getString("COUNT(id)"));

            }

        } catch (Exception e) {
            logger.log(Level.WARNING, "Home_count_accounts", e);
        }

    }

    private void countBooks() {

        try {
            ResultSet resultSet = MySQL.execute("SELECT COUNT(isbn) FROM `book`");

            if (resultSet.next()) {

                jLabel60.setText(resultSet.getString("COUNT(isbn)"));

            }

        } catch (Exception e) {
            logger.log(Level.WARNING, "Home_count_books", e);
        }

    }

    private void countUsers() {

        try {
            ResultSet resultSet = MySQL.execute("SELECT COUNT(email) FROM `user`");

            if (resultSet.next()) {

                jLabel65.setText(resultSet.getString("COUNT(email)"));

            }

        } catch (Exception e) {
            logger.log(Level.WARNING, "Home_count_users", e);
        }

    }

    private void countIssuedBooks() {

        try {
            ResultSet resultSet = MySQL.execute("SELECT COUNT(book_isbn) FROM `reservation_item` WHERE `is_return_id`='1'");

            if (resultSet.next()) {

                jLabel62.setText(resultSet.getString("COUNT(book_isbn)"));

            }

        } catch (Exception e) {
            logger.log(Level.WARNING, "Home_count_issued_books", e);
        }

    }

