    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:

        try {

            String email = jTextField2.getText();

            ResultSet resultSet = MySQL.execute("SELECT * FROM `user` INNER JOIN `user_type` ON "
                    + "`user`.`user_type_id`=`user_type`.`id` WHERE `email`='" + email + "'");

            if (resultSet.next()) {

                String userType = resultSet.getString("user_type.type");

                if (userType.equals("Super Admin")) {

                    UserRegistration userReg = new UserRegistration();
                    userReg.setVisible(true);

                } else if (userType.equals("Librarian")) {

                    UserRegistration userReg = new UserRegistration();
                    userReg.setVisible(true);

                } else {

                    JOptionPane.showMessageDialog(this, "Only Admin or Librarian can ADD or UPDATE users", "Warning", JOptionPane.WARNING_MESSAGE);

                }

            }
        } catch (Exception e) {
            logger.log(Level.WARNING, "Home_add_new_user", e);
        }


    }//GEN-LAST:event_jButton6ActionPerformed

