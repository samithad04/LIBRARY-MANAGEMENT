    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        String borrower = jTextField3.getText();
        String ISBN = jTextField5.getText();
        String title = jTextField8.getText();
        String author = jTextField6.getText();
        Date borrowDate = jDateChooser2.getDate();
        Date dueDate = jDateChooser1.getDate();

        if (borrower.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Please Select Borrower", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (ISBN.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Please Select A Book", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (borrowDate == null) {

            JOptionPane.showMessageDialog(this, "Please Select Borrowing Date(Currunt Date)", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (dueDate == null) {

            JOptionPane.showMessageDialog(this, "Please Select Due Date", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            ReservationItem reservationItem = new ReservationItem();
            reservationItem.setISBN(ISBN);
            reservationItem.setTitle(title);
            reservationItem.setAuthor(author);
            reservationItem.setBorrowDate(borrowDate);
            reservationItem.setDueDate(dueDate);

            if (bookItemMap.get(ISBN) == null) {
                bookItemMap.put(ISBN, reservationItem);
            } else {

                JOptionPane.showMessageDialog(this, "Book Already Added to Table", "Warning", JOptionPane.WARNING_MESSAGE);

            }

            loadBookItems();
            jTextField5.setText("");
            jTextField6.setText("");
            jTextField8.setText("");

        }

    }//GEN-LAST:event_jButton3ActionPerformed

