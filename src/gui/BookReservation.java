    private void loadBookItems() {

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for (ReservationItem reservationItem : bookItemMap.values()) {

            Vector<String> v = new Vector<>();
            v.add(reservationItem.getISBN());
            v.add(reservationItem.getTitle());
            v.add(reservationItem.getAuthor());
            v.add(sdf.format(reservationItem.getBorrowDate()));
            v.add(sdf.format(reservationItem.getDueDate()));

            model.addRow(v);
        }
    }

